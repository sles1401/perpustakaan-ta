    private void txtcariKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtcariKeyReleased
        try {

            DefaultTableModel tm = (DefaultTableModel) table.getModel();
            tm.setRowCount(0);
            int cariindex = cmbapa.getSelectedIndex();
            if (cariindex == 0) {
                koneksi.bahasasql = "SELECT * FROM buku WHERE id_buku LIKE '%" + txtcari.getText() + "%'";
            } else if (cariindex == 1) {
                koneksi.bahasasql = "SELECT * FROM buku WHERE judul LIKE '%" + txtcari.getText() + "%'";
            } else if (cariindex == 2) {
                koneksi.bahasasql = "SELECT * FROM buku WHERE pengarang LIKE '%" + txtcari.getText() + "%'";
            } else {
                koneksi.bahasasql = "SELECT * FROM buku WHERE tahun LIKE '%" + txtcari.getText() + "%'";
            }

            koneksi.ambildata();
            while (koneksi.hasilset.next()) {
                String id, judul, pengarang, penerbit, jenis, tahun, jumlah;

                id = koneksi.hasilset.getString("id_buku");
                judul = koneksi.hasilset.getString("judul");
                pengarang = koneksi.hasilset.getString("pengarang");
                penerbit = koneksi.hasilset.getString("penerbit");
                tahun = koneksi.hasilset.getString("tahun");
                jumlah = koneksi.hasilset.getString("jumlah");
                jenis = koneksi.hasilset.getString("jenis");
                tm.addRow(new Object[]{id, judul, pengarang, penerbit, tahun, jenis, jumlah});
            }
        } catch (SQLException ex) {
            Logger.getLogger(book.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_txtcariKeyReleased

