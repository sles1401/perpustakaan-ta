    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String id, judul, pengarang, penerbit, jenis;
            int tahun, jumlah, pilih;
            id = txtid.getText();
            judul = txtjudul.getText();
            pengarang = txtpengarang.getText();
            penerbit = txtpenerbit.getText();
            tahun = jy.getYear();
            jumlah = (int) spn.getValue();
            pilih = cmbjenis.getSelectedIndex();
            if (pilih == 0) {
                jenis = "Paket";
            } else if (pilih == 1) {
                jenis = "Modul";
            } else {
                jenis = "Fiksi";
            }
            if (id.equals("") || jumlah < 1 || judul.equals("") || pengarang.equals("") || penerbit.equals("")) {
                JOptionPane.showMessageDialog(this, "Lengkapi Kembali Data Anda");
            } else {
                koneksi.bahasasql = "INSERT INTO buku VALUES('" + id + "','" + judul + "','" + pengarang + "'"
                        + ",'" + penerbit + "','" + tahun + "','" + jenis + "','" + jumlah + "')";
                koneksi.crud();

                txtid.setText("");
                txtjudul.setText("");
                txtpengarang.setText("");
                txtpenerbit.setText("");
                jy.setYear(2019);
                spn.setValue(0);
                cmbjenis.removeAllItems();
                td.tampildatabuku(table);
                JOptionPane.showMessageDialog(this, "Data Berhasil Disimpan");
                auto.kodebuku(txtid);
            }

        } catch (SQLException ex) {
            Logger.getLogger(book.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Gagal " + ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

