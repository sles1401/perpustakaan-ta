    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        int total = Integer.valueOf(txttotal.getText());
        if (total > 3) {
            JOptionPane.showMessageDialog(this, "Maksimal Pinjam 3");
        } else {
            try {
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                String nopinjam, pinjam, kembali, nis, nama, idpet, namapet;
                nopinjam = txtnopinjam.getText();
                kembali = sdf.format(tglkembali.getDate());
                nis = txtnis.getText();
                nama = txtnama.getText();
                idpet = kf.m1.labelkod.getText();
                namapet = kf.m1.labeluser.getText();
                koneksi.bahasasql = "INSERT INTO headpin VALUES('" + nopinjam + "',CURRENT_DATE,'" + kembali + "','" + nis + "','" + nama + "','" + idpet + "','" + namapet + "')";
                koneksi.crud();

                int jumlah;
                for (int i = 0; i < tabletransaksi.getRowCount(); i++) {
                    String id = tabletransaksi.getValueAt(i, 0).toString();
                    String b = tabletransaksi.getValueAt(i, 1).toString();
                    String j = tabletransaksi.getValueAt(i, 2).toString();
                    koneksi.bahasasql = "INSERT INTO detailpem VALUES('" + nopinjam + "','" + id + "','" + b + "','" + j + "')";
                    koneksi.crud();
                    koneksi.bahasasql = "UPDATE buku SET jumlah=jumlah-'" + j + "' WHERE id_buku='" + id + "'";
                    koneksi.crud();

                }
                koneksi.koneksiku();
                JasperDesign JD = JRXmlLoader.load("src\\laporan\\struk.jrxml");
                JRDesignQuery JDQ = new JRDesignQuery();
                JDQ.setText("SELECT detailpem.no_pinjam, detailpem.jumlah, headpin.no_pinjam, headpin.tgl_pinjam, headpin.tgl_kembali, buku.id_buku, buku.judul,anggota.nis, anggota.nama_siswa FROM headpin INNER JOIN detailpem ON headpin.no_pinjam = detailpem.no_pinjam INNER JOIN  buku ON detailpem.id_buku = buku.id_buku INNER JOIN anggota ON headpin.nis=anggota.nis WHERE headpin.no_pinjam='"+nopinjam+"'");
                JD.setQuery(JDQ);
                JasperReport JR = JasperCompileManager.compileReport(JD);
                JasperPrint JP = JasperFillManager.fillReport(JR, null, koneksi.koneksi);
                JasperViewer JV = new JasperViewer(JP, false);
                JV.setVisible(true);
                DefaultTableModel TM = (DefaultTableModel) tabletransaksi.getModel();
                TM.setRowCount(0);
                txttotal.setText("");
                txtnopinjam.setText("");
                tglpinjam.setDate(null);
                tglkembali.setDate(null);
                txtnis.setText("");
                txtnama.setText("");
                JOptionPane.showMessageDialog(this, "Data Berhasil Disimpan");
                auto.kodepinjam(txtnopinjam);

            } catch (SQLException ex) {
                Logger.getLogger(peminjaman.class.getName()).log(Level.SEVERE, null, ex);
            } catch (JRException ex) {
                Logger.getLogger(peminjaman.class.getName()).log(Level.SEVERE, null, ex);
            }

        }


    }//GEN-LAST:event_jButton3ActionPerformed

