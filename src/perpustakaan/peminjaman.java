    private void tabletransaksiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabletransaksiMouseClicked

        try {
            String pilihid = tabletransaksi.getValueAt(tabletransaksi.getSelectedRow(), 0).toString();
            koneksi.bahasasql = "SELECT * FROM headpin INNER JOIN detailpen ON headpin.no_pinjam=detailpem.no_pinjam WHERE headpin.no_pinjam='" + pilihid + "'";
            koneksi.ambildata();
            while (koneksi.hasilset.next()) {
                String npinjam, nis, idp, kb, nama, namapet, judul;
                int jumlah;
                Date tglp, tglk;
                npinjam = koneksi.hasilset.getString("no_pinjam");
                tglp = koneksi.hasilset.getDate("tgl_pinjam");
                tglk = koneksi.hasilset.getDate("tgl_kembali");
                nis = koneksi.hasilset.getString("nis");
                nama = koneksi.hasilset.getString("nama_siswa");
                idp = koneksi.hasilset.getString("id_petugas");
                namapet = koneksi.hasilset.getString("nama_petugas");
                kb = koneksi.hasilset.getString("id_buku");
                judul = koneksi.hasilset.getString("judul");
                jumlah = koneksi.hasilset.getInt("jumlah");

                txtnopinjam.setText(npinjam);
                tglpinjam.setDate(tglp);
                tglkembali.setDate(tglk);
                txtnis.setText(nis);
                txtidbuku.setText(kb);
                sfjumlah.setValue(jumlah);
                txtnama.setText(nama);
                txtjudul.setText(judul);
            }
        } catch (SQLException ex) {
            Logger.getLogger(peminjaman.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tabletransaksiMouseClicked

